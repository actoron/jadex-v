package jadex.apmn;

public class Mission
{
    private int id;
    private String belief;
    private String goal;

    public int getId()
    {
        return id;
    }

    public String getBelief()
    {
        return belief;
    }

    public String getGoal()
    {
        return goal;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public void setBelief(String belief)
    {
        this.belief = belief;
    }

    public void setGoal(String goal)
    {
        this.goal = goal;
    }
}
